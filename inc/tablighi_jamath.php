<h1>Tablighi Jamath</h1>
<p>Welcome to WordPress plugin for Tablighi Jamaat. Here we can add data all persons around masjid and divide into places / areas.</p>
<p>While doing Gasth, we can easily identify the person and write down their response.</p>
<h2>Get started.</h2>
<p>Add your country and then add state. Similarly add district, town/village. Now add your Masjid. After adding masjid you can add places / areas /streets around that masjid.</p>
<p>After that go masjid page and start adding persons.</p>
<hr>
<h1>How to create Masjid List page</h1>
<p>WP Dashboard > Add New Page. In the content add this shortcode and publish the page.</p>
<pre>[masjid_persons]</pre>